var filters = {}


/*
  ====================================================================
  push
  --------------------------------------------------------------------
  push an item in to an array. Returns the array
  ====================================================================

  Usage:

  {% set array = ['a','b','c'] | push('d') %}

  array == ['a','b','c','d']

*/

filters.push = (array, item) => {
  array.push(item)
  return array
}







// -------------------------------------------------------------------
// keep the following line to return your filters to the app
// -------------------------------------------------------------------
exports.filters = filters
